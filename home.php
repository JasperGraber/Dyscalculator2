                    zijn door slecht onderwijs of didactische verwaarlozing. In dat geval wordt er niet gesproken van dyscalculie, maar van een rekenprobleem. De oorzaak moet dus in het kind liggen en niet in de omgeving. Dyscalculie komt ongeveer even
                    vaak voor als dyslexie.[1] Ongeveer 10% van alle kinderen heeft rekenproblemen en ongeveer 2% van alle kinderen is dyscalculisch.[2] De grens tussen rekenproblemen en dyscalculie is soms moeilijk te trekken. Uit onderzoek blijkt dat
                    er een samenhang is tussen dyscalculie en dyslexie.[1] Ook disharmonie in het IQ, zwak ruimtelijk inzicht en problemen met de aandacht zijn factoren die de ontwikkeling naar vloeiend rekenen kunnen belemmeren.[3]</p>
